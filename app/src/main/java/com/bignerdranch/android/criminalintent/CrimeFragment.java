    @Override
    public boolean onOptionsItemSelected(MenuItem item) {
        switch (item.getItemId()) {
            case R.id.delete_crime_action:
                CrimeLab.get(getActivity()).deleteCrime(mCrime.getmID());
                Toast.makeText(getActivity(), getString(R.string.toalst_delete_crime), Toast.LENGTH_SHORT).show();
                getActivity().finish();
                return true;

            default:
                return super.onOptionsItemSelected(item);
        }
    }

